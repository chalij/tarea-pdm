package sv.ues.fia.trabajograduacion;

public class TrabajoGraduacion 
{
	private int ntg=0;
	private int nperfil=0;
	private float porcentajea=0;
	public int getNtg() {
		return ntg;
	}
	public void setNtg(int ntg) {
		this.ntg = ntg;
	}
	public int getNperfil() {
		return nperfil;
	}
	public void setNperfil(int nperfil) {
		this.nperfil = nperfil;
	}
	public float getPorcentajea() {
		return porcentajea;
	}
	public void setPorcentajea(float porcentajea) {
		this.porcentajea = porcentajea;
	}

}
